import Foundation
import SwiftUI

enum Pallete {
        
    static var black = Color(red: 0, green: 0, blue: 0)
    static var white = Color(red: 255, green: 255, blue: 255)
    static var clear = Color(red: 255, green: 255, blue: 255, opacity: 0)
    
    static var blue = Color.blue
    static var utilityGray = Color(red: 60, green: 60, blue: 67)
}
